package in.ac.bvmengineering.udaan2k19.DataClass;

public class EventCategory {
    private int image;
    private String name;

    public EventCategory(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
